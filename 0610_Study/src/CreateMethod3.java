import java.util.Scanner;

public class CreateMethod3 {

	public static void printOddNum() {
		for (int i = 0; i <= 100; i++) {
			if (i % 2 != 0)
				System.out.println(i);
		}
	}

	public static void printEvenNum() {
		for (int i = 0; i <= 100; i++) {
			if (i % 2 == 0)
				System.out.println(i);
		}
	}

	public static void main(String[] args) {
		// 1. 콜라(1500) 2. 사이다 1400

	}
}
