public class Assign {
 public static void main(String[] args) {
	
	int num = 0;
	num = 80;
	num += 10; //복합대입연산자
	num -= 50;
	num *= 3;
	num /= 2;
	
	System.out.println(num);
	num++;
	System.out.println(num);
	++num;
	System.out.println(num++);
	System.out.println(num);
	num--;
	System.out.println(num);
	--num;
	System.out.println(num);
	
	
	
 }
 
}
	